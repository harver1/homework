# заданиче 3 с использованием списка

year_seasons = [['Зима', [1, 2, 12]], ['Весна', [3, 4, 5]], ['лето',  [6, 7, 8]], ['осень', [9, 10, 11]]]


month = int(input('Введите номер месяца: > '))
for season, months in year_seasons:
    if month in months:
        print(f'Введенный вами месяц соответсвует времени года - {season}')
